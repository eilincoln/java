package operacoesLogicas;

public class EstruturaFor {
	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			System.out.println("Numero atual é: " + i);
		}
		System.out.println("Fazendo ao contrário");
		for (int i = 10; i >= 0; i--) {
			System.out.println("numero é: " + i);
		}
	}
}
